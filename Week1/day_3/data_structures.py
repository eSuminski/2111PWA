# In Python we have Four basic ways of storing data within our code
# We can create lists, sets, tuples, and dictionaries

# Lists are the most versatile option for storing data: they are mutable, indexable, and they allow duplicates

# Sets are useful if you don't want to have duplicate items in it: they are mutable, do not allow duplicates, and they do not have indexing

# Tuples are useful when you need a data set to remain constant: they are not mutable, they allow duplicates and they do have indexing

# Dictionaries are key value pairs: they are mutable, no duplicate keys, and there are some versions of dictionaries that are indexable
# the normal version is not indexable
# In most programming languages there are primitive data types and objects
# primative data types are things like numbers and characters (a, b, c, etc).
# Objects are more complex pieces of data that have state(variables) and behaviors(methods)

# In Python there are NO primitives: only objects

my_small_number = 1
print(type(my_small_number))
# All objects have a class that acts as the blueprint that determines how the object is made and what its states and behaviors are

my_other_small_number = 1.1
print(type(my_other_small_number))

# again, THERE ARE NO PRIMITIVES IN PYTHON

